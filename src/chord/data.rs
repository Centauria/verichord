/// Retrieves the chord name associated with the given integer key.
/// This uses binary search for O(log n) efficiency.
pub fn get_chord_info(key: u32) -> Option<(&'static str, u32)> {
    CODEMAP
        .binary_search_by_key(&key, |&(k, _, _)| k)
        .ok()
        .map(|index| {
            let (_, name, base) = CODEMAP[index];
            (name, base)
        })
}
